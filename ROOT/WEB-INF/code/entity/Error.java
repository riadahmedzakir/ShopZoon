package entity;

public class Error {
   private String className;
   private String Error; 

   public String getClassName() {
       return className;
   }

   public void setClassName(String className) {
       this.className = className;
   }

   public String getError() {
       return Error;
   }

   public void setError(String error) {
       this.Error = error;
   }
   
}
